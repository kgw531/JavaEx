package com.jungang;

public class BmiEx {
	// BMI 구하기
	public static void main(String[] args) {
		// 변수 선언
		// 구조: 타입 변수이름;
		int weight = 50;
		double height = 1.6;
		
		double bmi = weight / (height * height);
		System.out.println("당신의 bmi는 " + bmi + " 입니다.");
		
	
		
	
	
	}

	

}

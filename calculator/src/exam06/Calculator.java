package exam06;

public class Calculator {
	
	
	/**
	 * 정사각형의 넓이 구하기
	 * @param width
	 * @return
	 */
	public double areaRectangle(double width) {
		return width * width;
	}
	
	/**
	 * 
	 * @param width,height
	 * @return width*height
	 */
	public double areaRectangle(double width, double height) {
		return width * height;
	}
	

}
